package motejx.extensions.motionplus;

public class MotionPlusCalibrationData {

	private int yaw;
	private int roll;
	private int pitch;
	
	
	
	public int getYaw() {
		return yaw;
	}
	public void setYaw(int yaw) {
		this.yaw = yaw;
	}
	
	public int getRoll() {
		return roll;
	}
	public void setRoll(int roll) {
		this.roll = roll;
	}
	
	public int getPitch() {
		return pitch;
	}
	public void setPitch(int pitch) {
		this.pitch = pitch;
	}

	
	
	
	
}
